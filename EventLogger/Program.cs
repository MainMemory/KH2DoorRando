		static void Main(string[] args)
		{
			rooms = JsonConvert.DeserializeObject<Room[]>(File.ReadAllText(@"D:\Documents\GitHub\KH2DoorRando\KH2DoorRando\Door_Rando.json"));
			foreach (Room r in rooms)
			{
				if (!roomdict.TryGetValue(r.World, out Dictionary<int, Room> world))
				{
					world = new Dictionary<int, Room>();
					roomdict.Add(r.World, world);
				}
				world.Add(r.ID, r);
			}
			timer.Elapsed += Timer_Elapsed;
			timer.Start();
			Console.ReadKey(true);
		}

		private static void Timer_Elapsed(object sender, ElapsedEventArgs e)
		{
			bool newproc = false;
			if (proc == null)
			{
				var pl = Process.GetProcessesByName("KINGDOM HEARTS II FINAL MIX");
				if (pl.Length > 0)
				{
					proc = pl[0];
					proc.EnableRaisingEvents = true;
					proc.Exited += Proc_Exited;
					var ba = proc.MainModule.BaseAddress;
					Now = ba + 0x0714DB8;
					Save = ba + 0x09A70B0;
					lastRoom = null;
					foreach (var rm in rooms)
					{
						rm.Map = proc.ReadUInt16(Save + 0x10 + 0x180 * rm.World + 0x6 * rm.ID);
						rm.Battle = proc.ReadUInt16(Save + 0x10 + 0x180 * rm.World + 0x6 * rm.ID + 2);
						rm.Event = proc.ReadUInt16(Save + 0x10 + 0x180 * rm.World + 0x6 * rm.ID + 4);
					}
					byte[] flags = proc.ReadBytes(Save + 0x1C90, 640);
					foreach (var pf in Progress)
					{
						pf.Set = (flags[pf.Byte] & pf.Mask) != 0;
					}
					Console.WriteLine("Attached to process {0}!", proc.Id);
					newproc = true;
				}
				else
					return;
			}
			lock (proc)
			{
				Room room = null;
				if (roomdict.TryGetValue(proc.ReadByte(Now), out var world))
					world.TryGetValue(proc.ReadByte(Now + 1), out room);
				if (room != lastRoom || newproc)
				{
					if (room != null)
						Console.WriteLine("Entered room {0}", room.Name);
					else
						Console.WriteLine("Entered unknown room");
					lastRoom = room;
				}
				foreach (var rm in rooms)
				{
					ushort map = proc.ReadUInt16(Save + 0x10 + 0x180 * rm.World + 0x6 * rm.ID);
					if (rm.Map != map)
					{
						Console.WriteLine("MAP change: {0} {1} -> {2}", rm.Name, rm.Map, map);
						rm.Map = map;
					}
					ushort btl = proc.ReadUInt16(Save + 0x10 + 0x180 * rm.World + 0x6 * rm.ID + 2);
					if (rm.Battle != btl)
					{
						Console.WriteLine("BTL change: {0} {1} -> {2}", rm.Name, rm.Battle, btl);
						rm.Battle = btl;
					}
					ushort evt = proc.ReadUInt16(Save + 0x10 + 0x180 * rm.World + 0x6 * rm.ID + 4);
					if (rm.Event != evt)
					{
						Console.WriteLine("EVT change: {0} {1} -> {2}", rm.Name, rm.Event, evt);
						rm.Event = evt;
					}
				}
				byte[] flags = proc.ReadBytes(Save + 0x1C90, 640);
				foreach (var pf in Progress)
				{
					bool nf = (flags[pf.Byte] & pf.Mask) != 0;
					if (nf != pf.Set)
					{
						Console.WriteLine("Flag change: {0} = {1}", pf.Name, nf);
						pf.Set = nf;
					}
				}
			}
		}

		private static void Proc_Exited(object sender, EventArgs e)
		{
			lock (proc)
				proc = null;
			Console.WriteLine("Process exited.");
		}
